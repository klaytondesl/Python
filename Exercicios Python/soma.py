# Faça um Programa que peça dois números e imprima a soma.

n1 = int(input("Digite o primeiro número: ")) # Recebe o número digitado e atribuia à variável [n1]
n2 = int(input("Digite o segundo número: ")) # Recebe o número digitado e atribuia à variável [n2]
soma = n1+n2 # Recebe a soma da variável [n1] com [n2]

print("A soma de {} + {} é: {}".format(n1,n2,soma)) # Imprime a soma dentro da variável [soma]